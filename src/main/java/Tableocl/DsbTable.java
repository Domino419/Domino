package Tableocl;

public class DsbTable {
	private int Num ;    
	private String Kind ;   
	private String Kinum ;      
	private String State ;
	public int getNum() {
		return Num;
	}
	public void setNum(int num) {
		Num = num;
	}
	public String getKind() {
		return Kind;
	}
	public void setKind(String kind) {
		Kind = kind;
	}
	public String getKinum() {
		return Kinum;
	}
	public void setKinum(String kinum) {
		Kinum = kinum;
	}
	public String getState() {
		return State;
	}
	public void setState(String state) {
		State = state;
	}   
	
	
	
	
}
