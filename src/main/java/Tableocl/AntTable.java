package Tableocl;

public class AntTable {
	private int Num ;   
	private String Name ;  
	private String AntDate ;  
	private String Time ;
	
	
	
	public int getNum() {
		return Num;
	}
	public void setNum(int num) {
		Num = num;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getAntDate() {
		return AntDate;
	}
	public void setAntDate(String antDate) {
		AntDate = antDate;
	}
	public String getTime() {
		return Time;
	}
	public void setTime(String time) {
		Time = time;
	}  
	

	
	
	
	
}
